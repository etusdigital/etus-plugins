def cmd_linear(args):
    if len(args) < 2:
        print("Usage: linear <etus_id> <linear_id> [<title>] [<type>] [<status>]", file=sys.stderr)
        sys.exit(1)
    etus_id   = args[0]
    linear_id = args[1]
    title     = args[2] if len(args) > 2 else ""
    ltype     = args[3] if len(args) > 3 else ""
    status    = args[4] if len(args) > 4 else "todo"
    now       = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    conn = get_conn()
    conn.execute("""
        INSERT INTO linear_mapping (etus_id, linear_id, title, type, status, created_at, updated_at)
        VALUES (?,?,?,?,?,?,?)
        ON CONFLICT(etus_id) DO UPDATE SET
            linear_id  = excluded.linear_id,
            title      = excluded.title,
            type       = excluded.type,
            status     = excluded.status,
            updated_at = excluded.updated_at
    """, (etus_id, linear_id, title, ltype, status, now, now))
    try:
        save_view("linear-mapping.md", render_linear_mapping(conn))
    except Exception:
        conn.rollback()
        raise
    conn.commit()
    conn.close()

    local_sync_note = ""
    project_slug = find_single_project_slug(PROJECT_ROOT)
    if project_slug:
        try:
            sync_path = sync_linear_mapping(
                PROJECT_ROOT,
                project_slug,
                etus_id,
                linear_id,
                title=title,
                item_type=ltype,
                status=status,
            )
            local_sync_note = f" | local sync: {sync_path}"
        except Exception as exc:
            local_sync_note = f" | local sync skipped: {type(exc).__name__}"

    print(f"✓ Linear mapping saved: {etus_id} → {linear_id}{local_sync_note}")


def cmd_supersede(args):
    if len(args) < 1:
        print("Usage: supersede <DEC-NNN> [<reason>]", file=sys.stderr)
        sys.exit(1)
    dec_ref = args[0]
    reason  = args[1] if len(args) > 1 else "superseded"
    # Extract numeric ID
    match = re.search(r"(\d+)", dec_ref)
    if not match:
        print(f"[memory-write] ERROR: Could not parse decision ID from '{dec_ref}'", file=sys.stderr)
        sys.exit(1)
    dec_id = int(match.group(1))

    conn = get_conn()
    row = conn.execute("SELECT id FROM decisions WHERE id=?", (dec_id,)).fetchone()
    if not row:
        print(f"[memory-write] ERROR: Decision DEC-{dec_id:03d} not found", file=sys.stderr)
        conn.close()
        sys.exit(1)
    conn.execute("UPDATE decisions SET status='superseded', rationale=COALESCE(rationale,'')||' [Superseded: '||?||']' WHERE id=?",
                 (reason, dec_id))
    try:
        save_view("decisions.md", render_decisions(conn))
    except Exception:
        conn.rollback()
        raise
    conn.commit()
    conn.close()
    print(f"✓ Decision DEC-{dec_id:03d} marked as superseded: {reason}")


def cmd_query(args):
    if not args:
        print("Usage: query <decisions|preferences|patterns|linear> [--phase <p>] [--tag <t>]", file=sys.stderr)
        sys.exit(1)

    table    = args[0]
    phase    = None
    tag      = None
    i = 1
    while i < len(args):
        if args[i] == "--phase" and i + 1 < len(args):
            phase = args[i + 1]; i += 2
        elif args[i] == "--tag" and i + 1 < len(args):
            tag = args[i + 1]; i += 2
        else:
            i += 1

    conn = get_conn()

    if table == "decisions":
        q = "SELECT id, decision, rationale, phase, tags, status FROM decisions WHERE 1=1"
        params = []
        if phase:
            q += " AND phase=?"; params.append(phase)
        if tag:
            q += " AND tags LIKE ?"; params.append(f'%"{tag}"%')
        q += " AND status='active' ORDER BY created_at DESC"
        rows = conn.execute(q, params).fetchall()
        if not rows:
            print("(no decisions found)")
        else:
            for r in rows:
                tags = ", ".join(json.loads(r["tags"])) if r["tags"] else ""
                print(f"DEC-{r['id']:03d} [{r['phase'] or '—'}] {r['decision']}")
                if r["rationale"]:
                    print(f"         → {r['rationale']}")
                if tags:
                    print(f"         tags: {tags}")

    elif table == "preferences":
        rows = conn.execute("SELECT id, preference, category FROM preferences ORDER BY category").fetchall()
        if not rows:
            print("(no preferences found)")
        else:
            for r in rows:
                print(f"PREF-{r['id']:03d} [{r['category'] or '—'}] {r['preference']}")

    elif table == "patterns":
        rows = conn.execute("SELECT id, pattern, applies_to FROM patterns ORDER BY created_at DESC").fetchall()
        if not rows:
            print("(no patterns found)")
        else:
            for r in rows:
                print(f"PAT-{r['id']:03d} [applies to: {r['applies_to']}] {r['pattern']}")

    elif table == "linear":
        rows = conn.execute("SELECT etus_id, linear_id, title, status FROM linear_mapping ORDER BY etus_id").fetchall()
        if not rows:
            print("(no linear mappings found)")
        else:
            for r in rows:
                print(f"{r['etus_id']} → {r['linear_id']}  [{r['status']}]  {r['title'] or ''}")

    elif table == "repo-docs":
        rows = conn.execute(
            "SELECT path, doc_type, status, updated_at FROM documents WHERE mode='reference' ORDER BY doc_type, path"
        ).fetchall()
        if not rows:
            print("(no repo docs indexed)")
        else:
            for r in rows:
                print(f"[{r['doc_type']}] {r['path']}  ({r['status']}, {r['updated_at'][:10]})")

    else:
        print(f"Unknown table: {table}. Choose: decisions, preferences, patterns, linear, repo-docs", file=sys.stderr)
        sys.exit(1)

    conn.close()


def cmd_index_docs(args):
    """Scan docs/ for .md files and index them into the memory system."""
    import importlib.util

    init_script = os.path.join(PROJECT_ROOT, ".claude/hooks/memory-init.py")
    if not os.path.exists(init_script):
        print("[memory-write] ERROR: memory-init.py not found", file=sys.stderr)
        sys.exit(2)

    spec = importlib.util.spec_from_file_location("memory_init", init_script)
    mod  = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)

    conn = get_conn()
    n = mod.migrate_existing_docs(conn)
    print(f"✓ Indexed {n} documents from docs/")

    # Regenerate all views
    mod.MEMORY_DIR = MEMORY_DIR  # ensure it uses our MEMORY_DIR
    mod.regenerate_all_views(conn)
    print("✓ All memory views regenerated (including repo-docs.md)")
    conn.close()


# ─────────────────────────────────────────────────────────────────────────────
# Entry point
# ─────────────────────────────────────────────────────────────────────────────
def main():
    args = sys.argv[1:]

    if not args or args[0] in ("-h", "--help"):
        print(HELP)
        sys.exit(0)

    command = args[0].lower()
    rest    = args[1:]

    dispatch = {
        "decision":    cmd_decision,
        "preference":  cmd_preference,
        "pattern":     cmd_pattern,
        "linear":      cmd_linear,
        "supersede":   cmd_supersede,
        "query":       cmd_query,
        "index-docs":  cmd_index_docs,
    }

    if command not in dispatch:
        print(f"Unknown command: {command}", file=sys.stderr)
        print(f"Available: {', '.join(dispatch.keys())}", file=sys.stderr)
        sys.exit(1)

    try:
        dispatch[command](rest)
    except SystemExit:
        raise
    except Exception as e:
        print(f"[memory-write] ERROR: {e}", file=sys.stderr)
        print(traceback.format_exc(), file=sys.stderr)
        sys.exit(2)


if __name__ == "__main__":
    main()

#!/usr/bin/env python3
"""
memory-write.py — Skill wrapper for writing to the ETUS PMDocs memory system.

Called by skills (via Bash tool) when they detect a decision, preference,
pattern, or linear mapping during a session. Inserts into SQLite and
regenerates the corresponding markdown view.

Usage:
  python3 .claude/hooks/memory-write.py decision  "<text>" "<rationale>" "<skill>" "<phase>" "<tag1,tag2>"
  python3 .claude/hooks/memory-write.py preference "<text>" "<skill>" "<category>"
  python3 .claude/hooks/memory-write.py pattern    "<text>" "<skill>" "<applies_to>"
  python3 .claude/hooks/memory-write.py linear     "<etus_id>" "<linear_id>" "<title>" "<type>" "<status>"
  python3 .claude/hooks/memory-write.py supersede  "<DEC-NNN>" "<reason>"
  python3 .claude/hooks/memory-write.py query      <decisions|preferences|patterns|linear> [--phase <phase>] [--tag <tag>]

Exit codes:
  0 — success
  1 — usage error (wrong args)
  2 — DB error
"""


def cmd_decision(args):
    if len(args) < 2:
        print("Usage: decision <text> [<rationale>] [<skill>] [<phase>] [<tags>]", file=sys.stderr)
        sys.exit(1)
    text      = args[0]
    rationale = args[1] if len(args) > 1 else ""
    skill     = args[2] if len(args) > 2 else ""
    phase     = args[3] if len(args) > 3 else ""
    tags_raw  = args[4] if len(args) > 4 else ""
    tags      = json.dumps([t.strip() for t in tags_raw.split(",") if t.strip()])
    now       = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")

    conn = get_conn()
    conn.execute(
        "INSERT INTO decisions (decision, rationale, source_skill, phase, tags, status, created_at) VALUES (?,?,?,?,?,'active',?)",
        (text, rationale, skill, phase, tags, now)
    )
    row_id = conn.execute("SELECT last_insert_rowid() as id").fetchone()["id"]
    try:
        save_view("decisions.md", render_decisions(conn))
    except Exception:
        conn.rollback()
        raise
    conn.commit()
    conn.close()
    print(f"✓ Decision logged: DEC-{row_id:03d} — {text}")


